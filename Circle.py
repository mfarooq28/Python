# This program will calculate the Area and Preimeter of the Circle
user_response = input("Enter the Radious of the Circle :")
radius = float (user_response)
Area = (radius * radius) * 3.14
Preimeter = (radius * 2) * 3.14

print ("The Area of the Circle is :" , Area , " and the Preimeter of the Circle is :", Preimeter)
